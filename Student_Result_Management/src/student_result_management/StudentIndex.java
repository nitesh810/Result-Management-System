    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:
        String rollNo = Roll.getText();
        try {
            Class.forName("com.mysql.cj.jdbc.Driver"); // creating instance...
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srms", "root", "niteshSTAR");
            
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select * from student s join result r using(rollNo) where s.rollNo = '"+rollNo+"' and r.rollNo = '"+rollNo+"' ");
            
            if(rs.next()) {
                jTextField1.setText(rs.getString(2)); // number inside is an index of all column in DB...
                jTextField3.setText(rs.getString(4));
                jTextField4.setText(rs.getString(3));
                jTextField5.setText(rs.getString(5));
                jTextField6.setText(rs.getString(6));
                jTextField7.setText(rs.getString(7));
                jTextField8.setText(rs.getString(8));
                jTextField9.setText(rs.getString(9));
                
                int total = Integer.parseInt(rs.getString(6)) +  Integer.parseInt(rs.getString(7)) + Integer.parseInt(rs.getString(8)) + Integer.parseInt(rs.getString(9));
                String t = total+"";
                jTextField10.setText(t);
            }
            
        } catch(Exception e) {
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_formComponentShown

