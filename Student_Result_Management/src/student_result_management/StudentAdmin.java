    private void SearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SearchActionPerformed
        // TODO add your handling code here:
        String roll = (String)Roll.getText();
        try {
            Class.forName("com.mysql.cj.jdbc.Driver"); // creating instance...
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srms", "root", "niteshSTAR");
            
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select * from result where rollno = '"+roll+"' "); // This will be null if there is no roll no is regestered inside the student table...
            
            if(rs.next()) { // checking roll no exists in the table...
                setVisible(false);
                new StudentIndex(roll).setVisible(true);
            } else {
                JOptionPane.showMessageDialog(null, "Incorrect roll number");
            }
            
        } catch(Exception e) {
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_SearchActionPerformed

