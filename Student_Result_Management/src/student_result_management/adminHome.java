    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        String course = (String)Course.getSelectedItem();
        String rollNo = (String)RollNo.getText();
        String name = (String)Name.getText();
        String gender = (String)Gender.getSelectedItem();
        String father = (String)Father.getText();
        try {
            Class.forName("com.mysql.cj.jdbc.Driver"); // creating instance...
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srms", "root", "niteshSTAR");
            
            Statement st = con.createStatement();
            st.executeUpdate("insert into student(rollno, course, name, gender, father) values('"+rollNo+"','"+course+"','"+name+"','"+gender+"','"+father+"')");
            JOptionPane.showMessageDialog(null, "Data saved successfully");
            setVisible(false);
            new adminHome().setVisible(true);
        } catch(Exception e) {
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_jButton6ActionPerformed

